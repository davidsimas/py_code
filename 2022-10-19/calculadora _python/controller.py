def soma(numero01, numero02):
    return numero01 + numero02


def subtracao(numero01, numero02):
    return numero01 - numero02


def multiplicacao(numero01, numero02):
    return numero01 * numero02


def divisao(numero01, numero02):
    return numero01 / numero02


def somaSalario(salario01, salario02, salario03, salario04):
    return salario01 + salario02 + salario03 + salario04

    